# Ввод массива
n = int(input("Введите количество элементов в массиве: "))
arr = []

# Заполнение массива
for i in range(n):
    value = float(input(f"Введите элемент {i+1}: "))
    arr.append(value)

# Задача 1: Работа с массивом как с целочисленным
print("\n" + "="*50)
print("Результаты обработки массива:")
print("="*50)

# 1.1 Поиск максимального элемента
int_arr = [int(x) for x in arr]  # Преобразуем в целые числа для первой задачи
max_element = max(int_arr)
print(f"1. Максимальный элемент (целочисленный): {max_element}")

# 1.2 Вывод массива в обратном порядке
print("2. Массив в обратном порядке:", *reversed(arr))

# Задача 2: Замена нулевых элементов
# 2.1 Рассчет среднего арифметического
avg = sum(arr) / n if n > 0 else 0

# 2.2 Замена нулевых элементов
modified_arr = arr.copy()  # Создаем копию для модификации
for i in range(n):
    if modified_arr[i] == 0:
        modified_arr[i] = avg

# 2.3 Проверка были ли изменения
if arr != modified_arr:
    print(f"3. Массив после замены нулей на среднее ({avg:.3f}):")
    print(*[f"{x:.3f}" for x in modified_arr])
else:
    print("3. В массиве не было нулевых элементов. Исходный массив:")
    print(*arr)

# Дополнительно: вывод обоих представлений массива
print("\n" + "-"*50)
print("Целочисленное представление:", *int_arr)
print("Исходное представление:    ", *[f"{x:.3f}" for x in arr])