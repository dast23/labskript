# Задача 1: Расчет стоимости конфет
print("Задача 1: Расчет стоимости конфет")
price = float(input("Введите цену за 1 кг конфет (руб): "))
print("Вес(кг) Стоимость(руб)")
for kg in range(1, 11):
    cost = price * kg
    print(f"{kg:5} {cost:15.2f}")

# Задача 2: Обработка последовательности чисел
print("\nЗадача 2: Обработка последовательности чисел")
total_sum = 0
count = 0

print("Вводите целые числа. Для завершения введите 0.")
num = int(input("Число: "))
while num != 0:
    total_sum += num
    count += 1
    num = int(input("Число: "))

if count == 0:
    print("Последовательность не содержит чисел!")
else:
    print(f"Сумма всех чисел: {total_sum}")
    print(f"Количество чисел: {count}")